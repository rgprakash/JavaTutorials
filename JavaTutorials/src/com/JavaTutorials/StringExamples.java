package com.JavaTutorials;

public class StringExamples {

	public static void main(String[] args) {
		
		String str1="Payment of $4500 made";
		System.out.println(str1.charAt(11));
		System.out.println(str1.indexOf("m"));
		System.out.println(str1.substring(11));
		System.out.println(str1.substring(11,21));
		System.out.println(str1.substring(0,16));
		

	}

}
